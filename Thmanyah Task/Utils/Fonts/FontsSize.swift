enum FontsSize: CGFloat, CaseIterable {
    // Small sizes
    case caption2 = 11
    case caption = 12
    case footnote = 13
    
    // Medium sizes
    case subheadline = 15
    case callout = 16
    case body = 17
    case headline = 18
    
    // Large sizes
    case title3 = 20
    case title2 = 22
    case title = 28
    case largeTitle = 34
    
    // Extra large sizes
    case extraLarge = 40
    case huge = 60
}
